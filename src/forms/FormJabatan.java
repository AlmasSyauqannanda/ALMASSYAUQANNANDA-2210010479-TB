    private void tambahButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tambahButtonActionPerformed
      try {
        Connection conn = DatabaseConnection.getConnection();
        String sql = "INSERT INTO jabatan (nama_jabatan, gaji_pokok) VALUES (?, ?)";
        PreparedStatement pst = conn.prepareStatement(sql);
        pst.setString(1, namaJabatanTextField.getText());
        pst.setInt(2, Integer.parseInt(gajiPokokTextField.getText()));
        pst.executeUpdate();
        JOptionPane.showMessageDialog(this, "Data berhasil ditambahkan!");
        tampilkanDataJabatan(); // Method untuk memperbarui tabel
    } catch (Exception e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
    }
    }//GEN-LAST:event_tambahButtonActionPerformed

    private void ubahButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ubahButtonActionPerformed
      try {
        Connection conn = DatabaseConnection.getConnection();
        int selectedRow = jabatanTable.getSelectedRow(); // Ambil baris yang dipilih
        if (selectedRow != -1) { // Pastikan ada baris yang dipilih
            String idJabatan = jabatanTable.getValueAt(selectedRow, 0).toString();
            String sql = "UPDATE jabatan SET nama_jabatan = ?, gaji_pokok = ? WHERE id_jabatan = ?";
            PreparedStatement pst = conn.prepareStatement(sql);
            pst.setString(1, namaJabatanTextField.getText());
            pst.setInt(2, Integer.parseInt(gajiPokokTextField.getText()));
            pst.setInt(3, Integer.parseInt(idJabatan));
            pst.executeUpdate();
            JOptionPane.showMessageDialog(this, "Data berhasil diubah!");
            tampilkanDataJabatan(); // Perbarui tabel
        } else {
            JOptionPane.showMessageDialog(this, "Pilih baris data yang ingin diubah.");
        }
    } catch (Exception e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
    }
    }//GEN-LAST:event_ubahButtonActionPerformed

    private void hapusButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_hapusButtonActionPerformed
      try {
        Connection conn = DatabaseConnection.getConnection();
        int selectedRow = jabatanTable.getSelectedRow(); // Ambil baris yang dipilih
        if (selectedRow != -1) { // Pastikan ada baris yang dipilih
            String idJabatan = jabatanTable.getValueAt(selectedRow, 0).toString();
            String sql = "DELETE FROM jabatan WHERE id_jabatan = ?";
            PreparedStatement pst = conn.prepareStatement(sql);
            pst.setInt(1, Integer.parseInt(idJabatan));
            pst.executeUpdate();
            JOptionPane.showMessageDialog(this, "Data berhasil dihapus!");
            tampilkanDataJabatan(); // Perbarui tabel
        } else {
            JOptionPane.showMessageDialog(this, "Pilih baris data yang ingin dihapus.");
        }
    } catch (Exception e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
    }
    }//GEN-LAST:event_hapusButtonActionPerformed

