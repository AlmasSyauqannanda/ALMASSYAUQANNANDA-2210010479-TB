   private void tampilkanDataJabatan() {
    try {
        Connection conn = DatabaseConnection.getConnection();
        String sql = "SELECT id_jabatan, nama_jabatan, gaji_pokok FROM jabatan";
        PreparedStatement pst = conn.prepareStatement(sql);
        ResultSet rs = pst.executeQuery();

        DefaultTableModel model = (DefaultTableModel) jabatanTable.getModel();
        model.setRowCount(0); // Reset tabel sebelum diisi ulang

        while (rs.next()) {
            Object[] row = {
                rs.getInt("id_jabatan"),
                rs.getString("nama_jabatan"),
                rs.getDouble("gaji_pokok")
            };
            model.addRow(row);
        }
    } catch (Exception e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(this, "Error saat memuat data jabatan: " + e.getMessage());
    }
}



