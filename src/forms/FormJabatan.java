    private void jabatanTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jabatanTableMouseClicked
        jabatanTable.addMouseListener(new java.awt.event.MouseAdapter() {
    public void mouseClicked(java.awt.event.MouseEvent evt) {
        int selectedRow = jabatanTable.getSelectedRow();

        // Pastikan baris yang diklik valid
        if (selectedRow >= 0) {
            String idJabatan = jabatanTable.getValueAt(selectedRow, 0).toString();
            String namaJabatan = jabatanTable.getValueAt(selectedRow, 1).toString();
            String gajiPokok = jabatanTable.getValueAt(selectedRow, 2).toString();

            // Tampilkan data di TextField
           
            namaJabatanTextField.setText(namaJabatan);
            gajiPokokTextField.setText(gajiPokok);
        }
    }
});

    }//GEN-LAST:event_jabatanTableMouseClicked

